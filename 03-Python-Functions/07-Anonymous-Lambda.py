

# lambda Function

# lambda Expressions ব্যবহার করে আমারা এক লাইনে Function লিখতে পারি ।
# lambda এর পরে স্পেস দিয়ে argument দিতে হবে তারপর : দিয়ে arithmetic expressions দিতে হবে ।


# Example
a = lambda a, b: a + b
print('sum :', a(20, 50))
print('\n')


# Example
f = lambda a: a*a
result = f(5)
print('Result :', result)
print('\n')



