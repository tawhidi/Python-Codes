# Membership Operators
print("Membership operators : \n")


"""
পাইথনে এই দুইটি অপারেটর আছে , in এন্ড not in
"""


# Example
a = "Bangladesh"
c = 'sh' in a
print("'sh' in a : ", c)


# Example
x = 'Hello world'
y = {1: 'F', 2: 'b'}
print("'H' in x : ", 'H' in x)


# Example
print("'hello' not in x : ", 'hello' not in x)
print(1 in y)
print('a' in y)
print('\n')
