#  Arithmetic operators
print("Arithmetic operators : \n")


"""
Operator        Description     Example

 +              addition        10 + 20 = 30
 -              Subtraction     20 - 10 = 10
 *              Multiplication  10 * 10 = 100
 /              Division        10 / 2  = 5
 %              Modulus         11 % 3  = 3.666666666
 **             power           3 ** 3  = 27
 //             Floor Division  11 // 3 = 3

"""


print("10 + 20 = ", 10 + 10)
print("20 - 10 = ", 20 - 10)
print("10 * 5  = ", 10 * 5)
print("11 / 3  = ", 11 / 3)
print("11 % 3  = ", 11 % 3)
print("11 // 3 = ", 11 // 3)
print("2 ** 3  = ", 2 ** 3)
print("\n")